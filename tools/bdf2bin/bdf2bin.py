        run_bdfconv(args.bdfconv, args.bdf_file, args.font_name,
                    args.map, c_file, args.verbose)

        # Step 2: Extract binary data from the C file
        font_data = extract_font_data(c_file, args.font_name)

        # Step 3: Write the binary data to the output file
        with open(bin_file, 'wb') as f:
            f.write(font_data)

        if args.verbose:
            print(f"Extracted {len(font_data)} bytes, written to {bin_file}")

